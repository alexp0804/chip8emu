int main(int argc, char **argv)
{
    CHIP8 chip;
    FILE *program_file;
    BYTE program_size, *program;
    SDL_Event event;
    SDL_Window *window;
    SDL_Renderer *renderer;
    SDL_Rect rects[WIDTH * HEIGHT];
    int i, j, running = 1, color;

    // Set up graphics
    SDL_Init(SDL_INIT_VIDEO);
    SDL_CreateWindowAndRenderer(width, height, 0, &window, &renderer);

    // Fill the rects array with SDL rectangles, these will represent our pixels
    // and will change height/width depending on the window size.
    for (i = 0; i < WIDTH; i++)
    {
        for (j = 0; j < HEIGHT; j++)
        {
            rects[IX(i, j)].w = width / WIDTH;
            rects[IX(i, j)].h = height / HEIGHT;
            rects[IX(i, j)].x = i * rects[IX(i, j)].w;
            rects[IX(i, j)].y = j * rects[IX(i, j)].h;
        }
    }

    // TODO: Set up input

    // Initialize chip
    init_chip(&chip);

    // Open ROM program file, get it's size
    program_file = fopen("/Users/alexp/Dev/chip8emu/roms/IBM Logo.ch8", "rb");
    fseek(program_file, 0, SEEK_END);
    program_size = ftell(program_file);
    rewind(program_file);

    // Allocate the array to hold the program
    program = calloc(program_size, sizeof(BYTE));

    // Rewind file pointer and read into the program array
    fread(program, sizeof(BYTE), program_size, program_file);

    // Load program into memory
    for (i = 0; i < 478; i++)
        chip.memory[i + 0x200] = test_prog[i];

    dump_memory(&chip);

    while (running)
    {
        uint64_t start = SDL_GetPerformanceCounter();

        while (SDL_PollEvent(&event))
            if (event.type == SDL_QUIT)
                running = 0;

        cycle(&chip);

        // If draw flag is set, update screen
        for (i = 0; i < WIDTH; i++)
        {
            for (j = 0; j < HEIGHT; j++)
            {
                color = (chip.screen[IX(i, j)] == 1) ? 255 : 0;
                SDL_SetRenderDrawColor(renderer, color, color, color, 255);
                SDL_RenderFillRect(renderer, &rects[IX(i, j)]);
            }
        }
        SDL_RenderPresent(renderer);
        uint64_t end = SDL_GetPerformanceCounter();

        // TODO: Store key press state (press and release)

        float elapsed = (end - start) / (float)SDL_GetPerformanceFrequency() * 1000.0f;
        float delay = 16.666f - elapsed;
        // Limit FPS to 60
        if (delay >= 0)
        {
            SDL_Delay(floor(16.666f - elapsed));
        }
    }

    SDL_DestroyRenderer(renderer);
    SDL_DestroyWindow(window);
    SDL_Quit();

    return 0;
}
